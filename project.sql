
CREATE TABLE IF NOT EXISTS Worker
(
  worker_id INTEGER PRIMARY KEY CHECK (worker_id >= 0),
  name      VARCHAR(50) NOT NULL,
  position  VARCHAR(50),
  chief_id  INTEGER REFERENCES Worker (worker_id)
);

CREATE TABLE IF NOT EXISTS Category
(
  cat_id  INTEGER PRIMARY KEY CHECK (cat_id >= 0),
  name    VARCHAR(20) NOT NULL,
  head_id INTEGER REFERENCES Worker (worker_id)
);

CREATE TABLE IF NOT EXISTS Dish
(
  dish_id    INTEGER PRIMARY KEY CHECK (dish_id >= 0),
  name       VARCHAR(100) NOT NULL UNIQUE,
  cat_id     INTEGER REFERENCES Category (cat_id),
  technology TEXT,
  diet_num   VARCHAR(10),
  cal_val    FLOAT(10)
);

CREATE TABLE IF NOT EXISTS Product
(
  product_id INTEGER PRIMARY KEY CHECK (product_id >= 0),
  name       VARCHAR(50) NOT NULL UNIQUE,
  unit       VARCHAR(10),
  CONSTRAINT chk_unit CHECK (unit IN ('кг', 'шт', 'л'))
);

CREATE TABLE IF NOT EXISTS ProductInDish
(
  dish_id    INTEGER NOT NULL REFERENCES Dish (dish_id),
  product_id INTEGER NOT NULL REFERENCES Product (product_id),
  gr_wt      FLOAT(10),
  nt_wt      FLOAT(10),
  CONSTRAINT pk_product_dish PRIMARY KEY (dish_id, product_id),
  CONSTRAINT chk_weight CHECK (gr_wt >= nt_wt)
);


CREATE TABLE IF NOT EXISTS Analog
(
  product_id  INTEGER NOT NULL REFERENCES Product (product_id),
  analog_id   INTEGER NOT NULL REFERENCES Product (product_id),
  coefficient FLOAT(10) DEFAULT 1.0,
  CONSTRAINT pk_analog PRIMARY KEY (analog_id, product_id)
);

CREATE TABLE IF NOT EXISTS Utensil
(
  utensil_id INTEGER PRIMARY KEY CHECK (utensil_id >= 0),
  name       VARCHAR(30) NOT NULL UNIQUE,
  type       VARCHAR(30)
);

CREATE TABLE IF NOT EXISTS UtensilForDish
(
  dish_id    INTEGER NOT NULL REFERENCES Dish (dish_id),
  utensil_id INTEGER NOT NULL REFERENCES Utensil (utensil_id),
  CONSTRAINT pk_utensil_dish PRIMARY KEY (dish_id, utensil_id)
);

